<h3>Instrumental Creation with Metatags</h3>
<p>Users can instruct SUNO AI to create instrumentals and songs by using specific metatags within square brackets. For example:</p>
<pre>[intro]
[monster beat]
[power]</pre>
<p><strong>Note that for instrumental creation (without lyrics), SUNO AI only accepts up to 200 characters.</strong></p>

